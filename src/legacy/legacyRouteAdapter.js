export const legacyBridgeRoutes = Object.keys(pageRoutes);

export function legacyIcon(name = "settings") {
  const stroke = iconStroke[name] || iconStroke.settings;
  return `<svg class="icon" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${stroke}</svg>`;
}

function removeLegacyHeader(markup) {
  return String(markup || "").replace(/\s*<header class="page-header"[\s\S]*?<\/header>\s*/g, "");
}

export function renderLegacyRouteBody(route, context = {}) {
  const render = pageRoutes[route] || pageRoutes["/home"];
  const markup = render({
    ...context,
    icon: context.icon || legacyIcon,
  });
  return removeLegacyHeader(markup);
}
